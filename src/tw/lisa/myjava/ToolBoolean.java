package tw.lisa.myjava;

public class ToolBoolean {

	public static void main(String[] args) {
		/*Java boolean資料型別
		 * 布林資料型別只有兩個有效值：true和false。
		 *true:int 
		 * false:0
		 */
		
		
		boolean a=true;
		if( a=true) {
			System.out.println("a is"+true);
		}else {
			System.out.println("a is"+false);
		}
		
		
		
	}

}
